public class Book {
    String nameBook;
    int yearBook;
    public Book(String nameBook, int yearBook) {
        this.nameBook = nameBook;
        this.yearBook = yearBook;
    }
    public void setYearBook(int yearBook) {
        this.yearBook = yearBook;
    }
    public String getNameBook() {
        return nameBook;
    }
    public int getYearBook() {
        return yearBook;
    }

}
